DEFAULT_COUNTRY = "England"
DEFAULT_PORT_ID = "113"


def isValidPortID( portID ):
	isValid = False
	if portID is not None and portID != "":
		for location in __locationsUnitedKingdomHydrographicOffice:
			if portID == location[ 2 ]:
				isValid = True

	return isValid


def getCountry( portID ): 
	country = DEFAULT_COUNTRY
	if portID is not None and portID != "":
		for location in __locationsUnitedKingdomHydrographicOffice:
			if portID == location[ 2 ]:
				country = location[ 0 ]

	return country


def getPortIDForCountry( country ):
	portID = DEFAULT_PORT_ID
	if country is not None and country != "":
		for location in __locationsUnitedKingdomHydrographicOffice:
			if country == location[ 0 ]:
				portID = location[ 2 ]
				break

	return portID


def getPortIDForCountryAndPort( country, port ):
	portID = DEFAULT_PORT_ID
	if country is not None and port is not None:
		for location in __locationsUnitedKingdomHydrographicOffice:
			if country == location[ 0 ] and port == location[ 1 ]:
				portID = location[ 2 ]
				break

	return portID


def getPortsForCountry( country ):
	if country is None or country not in getCountries():
		country = DEFAULT_COUNTRY

	ports = [ ]
	portIDs = [ ] # Used to track which port IDs have been added - don't want duplicates.
	for location in __locationsUnitedKingdomHydrographicOffice:
		if location[ 0 ] == country and location[ 1 ] not in ports and location[ 2 ] not in portIDs:
			ports.append( location[ 1 ] )
			portIDs.append( location[ 2 ] )

	return ports


def getPortForPortID( portID ):
	if not isValidPortID( portID ):
		portID = DEFAULT_PORT_ID

	# There are some ports which have the same port ID.
	# When the list of ports is returned to a caller, duplicate port IDs are not allowed and only the first port is returned.
	# Hence when searching for a port ID, the corresponding first port is returned which should exist in the list of ports.
	for location in __locationsUnitedKingdomHydrographicOffice:
		if portID == location[ 2 ]:
			return location[ 1 ]


def getCountries():
	countries = [ ]
	for location in __locationsUnitedKingdomHydrographicOffice:
		if location[ 0 ] not in countries:
			countries.append( location[ 0 ] )

	return countries


